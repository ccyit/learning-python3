# 变量与简单数据类型
message = "Hello Python World!"
print(message)
message = "Hello Python Crash Course world!"
print(message)
message = "This is a string."
print(message)

message = "This is also a string."
print(message)

message = 'I told my friend, "Python is my favorite language!"'
print(message)

message = "The language 'Python' is named after Monty Python, not the snake."
print(message)

message = "One of Python's strengths is its diverse and supportive community."
print(message)

# 练习
name = "Eric"
message = "Hello " + name + ", would you like to learn some Python today?"
print(message)

name = "albert einstein"

print(name.lower())
print(name.upper())
print(name.title())

famous_person = "Albert Einstein"
message = famous_person + ' once said, "A person who never made a mistake ' \
          'never tried anything new."'
print(message)

famous_person = "  \n\tAlert Einstein\n\t  "
print(famous_person)

print(famous_person.lstrip())
print(famous_person.rstrip())
print(famous_person.strip())
